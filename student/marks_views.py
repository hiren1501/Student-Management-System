@login_required
@user_passes_test(is_faculty)
def marks_upload(request):
    """Allow faculty to upload a CSV to bulk create/update StudentMarks.

    Expected CSV columns (headers recommended):
      - roll_number (preferred) OR student_id OR email
      - subject
      - exam_type  (one of StudentMarks.EXAM_TYPE_CHOICES values: midterm, final, quiz, assignment, project)
      - marks_obtained
      - total_marks (optional, defaults to 100)

    The view will attempt to locate students by roll_number first, then by id, then by email.
    A summary of processed/created/updated/failed rows is shown via messages.
    """
    try:
        faculty = faculties.objects.get(email=request.user.email)
    except faculties.DoesNotExist:
        messages.error(request, 'Faculty profile not found.')
        return redirect('faculty_welcome')

    if request.method == 'POST':
        csv_file = request.FILES.get('csv_file')
        if not csv_file:
            messages.error(request, 'No file uploaded. Please choose a CSV file.')
            return redirect('marks_upload')

        # Read and decode file safely
        try:
            decoded = csv_file.read().decode('utf-8')
        except Exception:
            try:
                decoded = csv_file.read().decode('latin-1')
            except Exception:
                messages.error(request, 'Could not decode uploaded file. Ensure it is a valid CSV (UTF-8 or Latin-1).')
                return redirect('marks_upload')

        reader = csv.DictReader(io.StringIO(decoded))
        if reader.fieldnames is None:
            messages.error(request, 'CSV appears empty or malformed. Ensure it has a header row.')
            return redirect('marks_upload')

        # Normalize expected header names
        processed = 0
        created = 0
        updated = 0
        failed = []

        # Acceptable exam_type values
        valid_exam_types = {k for k, _ in StudentMarks._meta.get_field('exam_type').choices}

        for i, row in enumerate(reader, start=2):
            processed += 1
            # Trim keys & values
            row = {k.strip(): (v.strip() if isinstance(v, str) else v) for k, v in row.items() if k}

            # Identify student
            student = None
            roll = row.get('roll_number') or row.get('roll') or row.get('student_roll')
            sid = row.get('student_id') or row.get('id')
            email = row.get('email')

            try:
                if roll:
                    student = Student.objects.get(roll_number=roll)
                elif sid:
                    student = Student.objects.get(id=int(sid))
                elif email:
                    student = Student.objects.get(email=email)
                else:
                    raise Student.DoesNotExist('No identifier provided')
            except Exception as e:
                failed.append(f'Row {i}: Student not found ({e})')
                continue

            subject = row.get('subject')
            exam_type = row.get('exam_type')
            marks_obtained = row.get('marks_obtained')
            total_marks = row.get('total_marks') or row.get('max_marks') or '100'

            if not subject:
                failed.append(f'Row {i}: Missing subject')
                continue

            # Normalize exam_type: if human label provided, try to map to key
            if exam_type and exam_type not in valid_exam_types:
                # try lowercase mapping
                exam_type_try = exam_type.strip().lower()
                # map common words
                mapping = {
                    'mid': 'midterm', 'mid-term': 'midterm', 'midterm': 'midterm',
                    'final': 'final', 'quiz': 'quiz', 'assignment': 'assignment', 'project': 'project'
                }
                exam_type = mapping.get(exam_type_try, exam_type_try if exam_type_try in valid_exam_types else None)

            if not exam_type:
                failed.append(f'Row {i}: Invalid or missing exam_type')
                continue

            try:
                marks_obtained = float(marks_obtained)
                total_marks = float(total_marks)
            except Exception:
                failed.append(f'Row {i}: marks_obtained/total_marks must be numeric')
                continue

            if marks_obtained < 0 or total_marks <= 0 or marks_obtained > total_marks:
                failed.append(f'Row {i}: Invalid marks range')
                continue

            # Create or update
            try:
                mark_obj, was_created = StudentMarks.objects.update_or_create(
                    student=student,
                    subject=subject,
                    exam_type=exam_type,
                    defaults={
                        'marks_obtained': marks_obtained,
                        'total_marks': total_marks,
                        'faculty': faculty,
                    }
                )
                if was_created:
                    created += 1
                else:
                    updated += 1
            except Exception as e:
                failed.append(f'Row {i}: DB error: {e}')
                continue

        # Build summary
        summary_lines = [f'Processed: {processed}', f'Created: {created}', f'Updated: {updated}']
        if failed:
            messages.error(request, 'Some rows failed to import. See details in server log and messages.')
            # Attach a short list to messages (limited)
            for msg in failed[:10]:
                messages.error(request, msg)
            if len(failed) > 10:
                messages.error(request, f'And {len(failed)-10} more errors...')
        else:
            messages.success(request, 'CSV import completed successfully.')

        for line in summary_lines:
            messages.info(request, line)

        return redirect('marks_list')

    # GET -> show upload form and instructions
    context = {
        'faculty': faculty,
        'exam_types': StudentMarks._meta.get_field('exam_type').choices,
    }
    return render(request, 'student/marks_upload.html', context)
