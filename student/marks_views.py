@login_required
@user_passes_test(is_faculty)
def marks_download(request, pk):
    """Return the generated marksheet PDF for the given mark record if it exists."""
    mark = get_object_or_404(StudentMarks, id=pk)
    # Build file path
    media_root = getattr(settings, 'MEDIA_ROOT', None)
    if not media_root:
        media_root = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), '..', 'media')
    file_path = os.path.join(media_root, 'marksheets', f'mark_{mark.id}.pdf')
    # If PDF missing, attempt to generate it now
    if not os.path.exists(file_path):
        try:
            generated = _generate_marks_pdf(mark)
            if generated:
                file_path = generated
            else:
                messages.error(request, 'Marksheet not found and could not be generated (ReportLab missing).')
                return redirect('marks_list')
        except Exception as e:
            messages.error(request, f'Error generating marksheet: {e}')
            return redirect('marks_list')

    try:
        return FileResponse(open(file_path, 'rb'), as_attachment=True, filename=os.path.basename(file_path))
    except Exception as e:
        messages.error(request, f'Could not open marksheet file: {e}')
        return redirect('marks_list')


@login_required
@user_passes_test(is_faculty)
def marks_email(request, pk):
    """Generate the PDF (if needed) and email it to the student for the given mark record."""
    mark = get_object_or_404(StudentMarks, id=pk)

    try:
        faculty = faculties.objects.get(email=request.user.email)
    except faculties.DoesNotExist:
        messages.error(request, 'Faculty profile not found.')
        return redirect('faculty_welcome')

    if mark.faculty != faculty:
        messages.error(request, 'You do not have permission to email this record.')
        return redirect('marks_list')

    # Send a plain-text email containing the marks details (no attachment)
    student_email = getattr(mark.student, 'email', None)
    if not student_email:
        messages.error(request, 'Student has no email configured.')
        return redirect('marks_list')

    # Build plain-text email body with the teacher-entered details
    student_name = getattr(mark.student, 'name', 'Student')
    roll = getattr(mark.student, 'roll_number', '')
    grade_display = mark.student.grade.get_grade_display() if getattr(mark.student, 'grade', None) else ''
    faculty_name = getattr(faculty, 'name', '')
    recorded_on = mark.date_recorded.strftime('%Y-%m-%d %H:%M') if getattr(mark, 'date_recorded', None) else ''
    body_lines = [
        f"Dear {student_name},",
        "",
        f"This is the marks report entered by your teacher:",
        "",
        f"Student: {student_name}",
        f"Roll Number: {roll}",
        f"Grade: {grade_display}",
        f"Subject: {mark.subject}",
        f"Exam Type: {mark.get_exam_type_display()}",
        f"Marks Obtained: {mark.marks_obtained} / {mark.total_marks}",
        f"Percentage: {mark.percentage:.2f}%",
        f"Recorded By: {faculty_name}",
        f"Recorded On: {recorded_on}",
        "",
        "If you have any questions, please contact your teacher.",
        "",
        f"Regards,",
        f"{faculty_name}",
    ]

    body = "\n".join(body_lines)

    try:
        email = EmailMessage(
            subject=f"Marks Report - {mark.subject} ({mark.get_exam_type_display()})",
            body=body,
            from_email=getattr(settings, 'DEFAULT_FROM_EMAIL', None),
            to=[student_email],
        )
        email.send(fail_silently=False)
        messages.success(request, 'Marks details emailed to the student successfully.')
    except Exception as e:
        messages.error(request, f'Could not send email: {e}')

    return redirect('marks_list')


